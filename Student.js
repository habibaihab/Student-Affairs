class Student{
    constructor(Name, ID, DateOfBirth, Status, GPA, Gender, Level, Department, Email, PhoneNumber){
        this.Name = Name;
        this.ID = ID;
        this.DateOfBirth = DateOfBirth;
        this.Status = Status;
        this.GPA = GPA;
        this.Gender = Gender;
        this.Level = Level;
        this.Department = Department;
        this.Email = Email;
        this.PhoneNumber = PhoneNumber;
    }
};


